# 1. capitalize()
a = "hello, and welcome to my world."

x = a.capitalize()

print (x)


# 2. center()
b = "banana"
x = b.center(20)

print(x)


# 3. count()
c = "I love apples, apple are my favorite fruit"

x = c.count("apple")

print(x)


# 4. index()
d = "Hello, welcome to my world."

x = d.index("welcome")

print(x)


# 5. replace()
e = "I like bananas"

x = e.replace("bananas", "apples")

print(x)


# 6. split()
f = "welcome to the jungle"

x = f.split()

print(x)


# 7. upper()
g = "Hello my friends"

x = g.upper()

print(x)


# 8. lower()
h = "Hello my FRIENDS"

x = h.lower()

print(x)